# String for demonstration
text = "Python Programming"

# Slicing examples
print("Original String:", text)

# 1. Slice from index 0 to 6
print("text[0:6]   ->", text[0:6])   # 'Python'

# 2. Slice from index 7 to end
print("text[7:]    ->", text[7:])    # 'Programming'

# 3. Slice from start to index 6
print("text[:6]    ->", text[:6])    # 'Python'

# 4. Slice with a step of 2
print("text[::2]   ->", text[::2])   # Every 2nd character

# 5. Reverse the string
print("text[::-1]  ->", text[::-1])
